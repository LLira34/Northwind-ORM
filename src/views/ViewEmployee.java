    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            DateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");

            Employee employee = new Employee();
            employee.setLastname(txtLastname.getText());
            employee.setFirstname(txtFirstname.getText());
            employee.setTitle(txtTitle.getText());

            Date bdate = new Date();
            bdate = dcsBirthdate.getDate();
            employee.setBirthdate(dateFormat.format(bdate));

            Date hdate = new Date();
            hdate = dcsHiredate.getDate();
            employee.setHiredate(dateFormat.format(hdate));

            employee.setCity(txtCity.getText());
            dao.saveOrUpdate(employee);
            this.clean();
            this.initTable();
        } catch (SiscomException ex) {
            Logger.getLogger(ViewCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

