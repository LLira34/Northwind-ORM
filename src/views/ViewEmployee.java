    private void tblEmployeesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEmployeesMouseClicked
        // TODO add your handling code here:
        int row = tblEmployees.rowAtPoint(evt.getPoint());
        int col = tblEmployees.columnAtPoint(evt.getPoint());
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        Date bdate, hdate;

        if (col == 7) {
            int answer = JOptionPane.showConfirmDialog(this, "¿Eliminar empleado?", "Eliminar Empleado",
                    JOptionPane.YES_NO_OPTION);
            if (answer == 0) {
                try {
                    int id = Integer.valueOf(tblEmployees.getValueAt(row, 0).toString());
                    dao.delete(id);
                    this.initTable();
                } catch (SiscomException ex) {
                    JOptionPane.showMessageDialog(this, "No se pudo eliminar el registro");
                }
            }
        }

        if (col == 8) {
            try {
                id = Integer.valueOf(tblEmployees.getValueAt(row, 0).toString());
                txtLastname.setText(tblEmployees.getValueAt(row, 1).toString());
                txtFirstname.setText(tblEmployees.getValueAt(row, 2).toString());
                txtTitle.setText(tblEmployees.getValueAt(row, 3).toString());

                bdate = simpleDateFormat.parse(tblEmployees.getValueAt(row, 4).toString());
                dcsBirthdate.setDate(bdate);
                hdate = simpleDateFormat.parse(tblEmployees.getValueAt(row, 5).toString());
                dcsHiredate.setDate(hdate);

                txtCity.setText(tblEmployees.getValueAt(row, 6).toString());
                btnSave.setEnabled(false);
                btnUpdate.setEnabled(true);
            } catch (ParseException ex) {
                Logger.getLogger(ViewEmployee.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_tblEmployeesMouseClicked

